#!/usr/bin/env python3
"""Builds the question sets into questions/<id>.json.
# NOTE: the questions/*.json files are the CANONICAL source. Several sets have been
# length/position-debiased directly in the JSON; regenerating from this script would
# revert that content. Edit the JSON files, or update the rows here first.

Each set file: { id, name, description, order, questions: [ {id, topic, text, options, correct, explanation} ] }
- Core is bootstrapped from the existing questions.json (or re-read from its own set file).
- Advanced and AI Foundations are defined inline below.
Edit a set by editing its JSON file directly; this script is just how they were created.
"""
import json, os, random

HERE = os.path.dirname(os.path.abspath(__file__))
QDIR = os.path.join(HERE, "questions")
os.makedirs(QDIR, exist_ok=True)


def load_core_questions():
    # Prefer the already-split set file; fall back to the legacy questions.json
    for path in [os.path.join(QDIR, "java-devops-core.json"), os.path.join(HERE, "questions.json")]:
        if os.path.exists(path):
            data = json.load(open(path))
            return data["questions"] if isinstance(data, dict) else data
    raise SystemExit("No core questions source found")


