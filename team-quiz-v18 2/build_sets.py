def to_questions(rows):
    out = []
    for i, (topic, text, options, correct, expl) in enumerate(rows, start=1):
        assert len(options) == 4 and 0 <= correct <= 3, f"bad row {i}"
        # Shuffle options deterministically (seeded per question) to avoid
        # answer-position bias — otherwise the correct answer is always 'B'.
        rng = random.Random(text)
        order = [0, 1, 2, 3]
        rng.shuffle(order)
        options = [options[j] for j in order]
        correct = order.index(correct)
        out.append({"id": f"q{i:03d}", "topic": topic, "text": text,
                    "options": options, "correct": correct, "explanation": expl})
    return out


def write_set(fid, name, description, order, questions):
    path = os.path.join(QDIR, fid + ".json")
    payload = {"id": fid, "name": name, "description": description, "order": order,
               "questions": questions}
    with open(path, "w") as f:
        json.dump(payload, f, indent=2, ensure_ascii=False)
        f.write("\n")
    print(f"  {fid}: {len(questions)} questions -> {name}")


